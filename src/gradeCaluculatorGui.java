    private void btnEntrGradesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrGradesActionPerformed
        // create the variables and any needed modifiers 
        DecimalFormat df = new DecimalFormat("#0.0##");
        
        String input = null;
        double mean;
        double median;
        int i = 0;
        
        //create array
        final int GRADES_LENGTH = 25;
        double[] gradeRecord = new double[GRADES_LENGTH];
        
        //now create the imput for the array and the loop
         do 
        {
            try
            {
               input=JOptionPane.showInputDialog (this, "Enter Grade.","Enter Grade", 
               JOptionPane.PLAIN_MESSAGE);
               if (input != null && input.length() > 0) 
               {
                   gradeRecord[i] = Double.parseDouble(input);
                    i++; 
               }
               if (i > 24) 
               {
                   JOptionPane.showMessageDialog(this, "You can only input 25 values!!","Too Much Data!",
                   JOptionPane.ERROR_MESSAGE);
               }
            }
            catch (NumberFormatException e) 
            {
                JOptionPane.showMessageDialog(this, "Your input must be numeric!","Invalid Data!",
                JOptionPane.ERROR_MESSAGE);
            }
        } while (input != null && input.length() > 0);
         
         txtGradeNum.setText(Integer.toString(i));
         
         mean = computeMean(gradeRecord, i);
         txtGradeMean.setText(df.format(mean));
         txtGradeMean.setText(Double.toString(mean));
         
         median = computeMedian(gradeRecord, i);
         txtGradeMedian.setText(df.format(median));
         txtGradeMedian.setText(Double.toString(median));
         
        
    }//GEN-LAST:event_btnEntrGradesActionPerformed

