    public static double computeMean (double[] gradeRecord, int i){
        double sum =0;
        for (int n = 0; n < i; n++) {
        sum += gradeRecord[n];
    }
    return sum / i;
    }
    
    public static double computeMedian(double[] gradeRecord, int i) {
         //Important sort first!!!!!!
         Arrays.sort(gradeRecord, 0, i);
        int middle = i/2;
        if (i%2 == 1) {
        return gradeRecord[middle];
    } else {
        return (gradeRecord[middle-1] + gradeRecord[middle]) / 2.0;
    }
}
